function getDailyQuote() {
    let startDate = new Date("2024-01-01");
    let today = new Date();
    let dayIndex = Math.floor((today - startDate) / (1000 * 60 * 60 * 24));
    let quoteIndex = dayIndex % quotes.length;
    document.getElementById("quote").textContent = quotes[quoteIndex];
}

function updateDateInfo() {
    const today = new Date();
    const options = { year: "numeric", month: "long", day: "numeric" }; // Exclude weekday

    // Get the day of the week and capitalize the first letter
    let dayOfWeek = today.toLocaleDateString("cs-CZ", { weekday: "long" });
    dayOfWeek = dayOfWeek.charAt(0).toUpperCase() + dayOfWeek.slice(1); // Capitalize the first letter

    document.getElementById("day-of-week").textContent = dayOfWeek;
    document.getElementById("current-date").textContent = today.toLocaleDateString("cs-CZ", options); // Display date without weekday
}
function updateFamilyPhoto() {
    const today = new Date();
    const dayOfYear = Math.floor((today - new Date(today.getFullYear(), 0, 0)) / 86400000); // Get day of the year (0-364)

    // Generate the filename based on the day of the year
    const photoFilename = `photos/photo${dayOfYear + 1}.jpg`; // Adds 1 because dayOfYear is 0-indexed

    // Update the src attribute of the image element
    document.getElementById("daily-photo").src = photoFilename;
}

